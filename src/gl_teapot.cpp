// NOTE: teapotPatches uses 1-indexed values in classic data.
// The vertex array above is already 0-indexed, but the patch indices
// reference vertices starting from 0. (This particular data set is 0-based.)

// Evaluate cubic Bezier basis
static void bernstein(float t, float b[4]) {
  float s = 1.0f - t;
  b[0] = s * s * s;
  b[1] = 3.0f * s * s * t;
  b[2] = 3.0f * s * t * t;
  b[3] = t * t * t;
}

// Derivative of cubic Bezier basis
static void bernsteinDeriv(float t, float db[4]) {
  float s = 1.0f - t;
  db[0] = -3.0f * s * s;
  db[1] = 3.0f * s * s - 6.0f * s * t;
  db[2] = 6.0f * s * t - 3.0f * t * t;
  db[3] = 3.0f * t * t;
}

struct Vec3 {
  float x, y, z;
  Vec3 operator+(const Vec3 &o) const { return {x + o.x, y + o.y, z + o.z}; }
  Vec3 operator*(float s) const { return {x * s, y * s, z * s}; }
};

static Vec3 cross(const Vec3 &a, const Vec3 &b) {
  return {a.y * b.z - a.z * b.y, a.z * b.x - a.x * b.z, a.x * b.y - a.y * b.x};
}

static Vec3 normalize(const Vec3 &v) {
  float len = std::sqrt(v.x * v.x + v.y * v.y + v.z * v.z);
  if (len < 1e-8f)
    return {0, 0, 1};
  return {v.x / len, v.y / len, v.z / len};
}

// Evaluate one Bezier patch at (u, v), also compute normal
static void evalPatch(const int patch[16], float u, float v, Vec3 &pos,
                      Vec3 &norm) {
  float bu[4], bv[4], dbu[4], dbv[4];
  bernstein(u, bu);
  bernstein(v, bv);
  bernsteinDeriv(u, dbu);
  bernsteinDeriv(v, dbv);

  pos = {0, 0, 0};
  Vec3 dpdu = {0, 0, 0};
  Vec3 dpdv = {0, 0, 0};

  for (int i = 0; i < 4; i++) {
    for (int j = 0; j < 4; j++) {
      int idx = patch[i * 4 + j];
      Vec3 cp = {teapotVerts[idx][0], teapotVerts[idx][1], teapotVerts[idx][2]};

      float w = bu[i] * bv[j];
      float wu = dbu[i] * bv[j];
      float wv = bu[i] * dbv[j];

      pos = pos + cp * w;
      dpdu = dpdu + cp * wu;
      dpdv = dpdv + cp * wv;
    }
  }

  norm = normalize(cross(dpdu, dpdv));
}

void renderTeapotGL(float size, int gridN) {
  // Fix 1: render both sides — classic teapot patches have inconsistent
  //         winding, so back-face culling punches holes
  glDisable(GL_CULL_FACE);
  glLightModeli(GL_LIGHT_MODEL_TWO_SIDE, GL_TRUE);

  float invN = 1.0f / (float)gridN;

  // Pre-allocate grid of positions and normals for one patch
  std::vector<Vec3> P((gridN + 1) * (gridN + 1));
  std::vector<Vec3> N((gridN + 1) * (gridN + 1));

  for (int p = 0; p < 32; p++) {
    // Evaluate the grid
    for (int i = 0; i <= gridN; i++) {
      float u = i * invN;
      for (int j = 0; j <= gridN; j++) {
        float v = j * invN;
        int k = i * (gridN + 1) + j;
        evalPatch(teapotPatches[p], u, v, P[k], N[k]);
      }
    }

    // Render as triangle strips
    for (int i = 0; i < gridN; i++) {
      glBegin(GL_TRIANGLE_STRIP);
      for (int j = 0; j <= gridN; j++) {
        int k0 = i * (gridN + 1) + j;
        int k1 = (i + 1) * (gridN + 1) + j;

        glNormal3f(N[k0].x, N[k0].y, N[k0].z);
        glVertex3f(P[k0].x * size, P[k0].y * size, P[k0].z * size);

        glNormal3f(N[k1].x, N[k1].y, N[k1].z);
        glVertex3f(P[k1].x * size, P[k1].y * size, P[k1].z * size);
      }
      glEnd();
    }
  }

  // Restore state
  glEnable(GL_CULL_FACE);
  glLightModeli(GL_LIGHT_MODEL_TWO_SIDE, GL_FALSE);
}